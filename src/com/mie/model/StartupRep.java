package com.mie.model;

public class StartupRep {
	
	private String name;
	private String position;
	private String introduction; 
	private String email;
	private String password;
	private String startup;
	
	private int startupID;
	
	public String getName() {
		return name;
	}
	
	public void setName(String new_name) {
		this.name = new_name;
	}
	
	public String getPosition() {
		return position;
	}
	
	public void setPosition(String new_position) {
		this.position = new_position;
	}
	
	public String getIntroduction() {
		return introduction;
	}
	
	public void setIntroduction(String new_introduction) {
		this.introduction = new_introduction;
	}
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String new_email) {
		this.email = new_email; 
	}
	
	public String getPassword() {
		return password;
	}
	
	public void setPassword(String new_password) {
		this.password = new_password; 
 	}
	
	public String getStartup() {
		return startup;
	}
	
	public void setStartup(String new_startup) {
		this.startup = new_startup;
	}
	
	public int getStartupID() {
		return startupID;
	}
	
	public void setStartupID(int new_startupID) {
		this.startupID = new_startupID;
	}
	
	

}
